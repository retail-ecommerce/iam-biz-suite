
package com.terapico.iam;


public class IamRuntimeException extends RuntimeException {
    static final long serialVersionUID = -1;

    public IamRuntimeException() {
        super();
    }


    public IamRuntimeException(String message) {
        super(message);
    }


    public IamRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }


    public IamRuntimeException(Throwable cause) {
        super(cause);
    }


   
}












